package sec03.exam01.day1;
//클래스 선언부
//public은 공개클래스 Hello는 클래스 이름, 클래스 이름은 소스 파일명과 동일해야 하며, 대소문자도 일치해야 한다.

public class Hello {//class block
	//method 선언부, main은 method의 이름
	public static void main(String[] args) {	//method block
		
		System.out.println("Hello, java");	//run 하면 method block 내부에 있는 명령문 실행, 실행문 종료시 ;(세미콜론)을 붙여줘야 한다.
	}//end method block

}//end class block
