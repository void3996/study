package sec02.exam06.day51;

public class WhilePrintFrom1To10Example {

	public static void main(String[] args) {
		int i = 1;
		while (i<=10) {
			System.out.println(i);
			i++;
		}	// i가 10을 초과할 때까지 while 문 반복 실행
//		while (1 조건식) {
//			조건식이 true일경우
//			2 실행문;
//		}
//		조건식이 true라면 1 조건식 평가
//		조건식이 true 라면 2 실행문 -> 1 조건식 진행
//		false 라면 { } 밖으로 빠져나오고 while 문 종료
		
	}

}
