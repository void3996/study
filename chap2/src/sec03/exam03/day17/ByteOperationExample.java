package sec03.exam03.day17;

public class ByteOperationExample {

	public static void main(String[] args) {
		byte result1 = 10 + 20;	//정수 연산 한것을 result1 변수에 담았다
		System.out.println(result1);
		
		byte x = 10;
		byte y = 20;
		int result2 = x + y;	//자동타입변환되어 byte 타입에 담긴 x,y의 값이 연산되어 result2 변수에 대입되었다.
		System.out.println(result2);

	}

}
